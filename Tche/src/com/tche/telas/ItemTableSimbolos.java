package com.tche.telas;

public class ItemTableSimbolos {

	// pila, bueno
	private String nomeTipoVal;
	// valor da variavel
	private String valorVal;
	// variavel ou funcao
	private String tipoEntrada;
	// nome da variavel
	private String nome;
	// tipo array
	private String tipArray;
	// dimensao
	private Integer dimensao;

	public String getNomeTipoVal() {
		return nomeTipoVal;
	}

	public void setNomeTipoVal(String nomeTipoVal) {
		this.nomeTipoVal = nomeTipoVal;
	}

	public String getValorVal() {
		return valorVal;
	}

	public void setValorVal(String valorVal) {
		this.valorVal = valorVal;
	}

	public String getTipoEntrada() {
		return tipoEntrada;
	}

	public void setTipoEntrada(String tipoEntrada) {
		this.tipoEntrada = tipoEntrada;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public String getTipArray() {
		return tipArray;
	}

	public void setTipArray(String tipArray) {
		this.tipArray = tipArray;
	}

	public Integer getDimensao() {
		return dimensao;
	}

	public void setDimensao(Integer dimensao) {
		this.dimensao = dimensao;
	}

}
